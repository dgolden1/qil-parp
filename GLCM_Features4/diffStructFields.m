function diff = diffStructFields(st1, st2, noffs)
    diff.autoc = 0;
    diff.contr = 0;
    diff.corrm = 0;
    diff.corrp = 0;
    diff.cprom = 0;
    diff.cshad = 0;
    diff.dissi = 0;
    diff.energ = 0;
    diff.entro = 0;
    diff.homom = 0;
    diff.homop = 0;
    diff.maxpr = 0;
    diff.sosvh = 0;
    diff.savgh = 0;
    diff.svarh = 0;
    diff.senth = 0;
    diff.dvarh = 0;
    diff.denth = 0;
    diff.inf1h = 0;
    diff.inf2h = 0;
    diff.indnc = 0;
    diff.idmnc = 0;
    for i = 1:noffs
        diff.autoc = diff.autoc + abs( st1.autoc(i) - st2.autoc(i) );
        diff.contr = diff.contr + abs( st1.contr(i) - st2.contr(i) );
        diff.corrm = diff.corrm + abs( st1.corrm(i) - st2.corrm(i) );
        diff.corrp = diff.corrp + abs( st1.corrp(i) - st2.corrp(i) );
        diff.cprom = diff.cprom + abs( st1.cprom(i) - st2.cprom(i) );
        diff.cshad = diff.cshad + abs( st1.cshad(i) - st2.cshad(i) );
        diff.dissi = diff.dissi + abs( st1.dissi(i) - st2.dissi(i) );
        diff.energ = diff.energ + abs( st1.energ(i) - st2.energ(i) );
        diff.entro = diff.entro + abs( st1.entro(i) - st2.entro(i) );
        diff.homom = diff.homom + abs( st1.homom(i) - st2.homom(i) );
        diff.homop = diff.homop + abs( st1.homop(i) - st2.homop(i) );
        diff.maxpr = diff.maxpr + abs( st1.maxpr(i) - st2.maxpr(i) );
        diff.sosvh = diff.sosvh + abs( st1.sosvh(i) - st2.sosvh(i) );
        diff.savgh = diff.savgh + abs( st1.savgh(i) - st2.savgh(i) );
        diff.svarh = diff.svarh + abs( st1.svarh(i) - st2.svarh(i) );
        diff.senth = diff.senth + abs( st1.senth(i) - st2.senth(i) );
        diff.dvarh = diff.dvarh + abs( st1.dvarh(i) - st2.dvarh(i) );
        diff.denth = diff.denth + abs( st1.denth(i) - st2.denth(i) );
        diff.inf1h = diff.inf1h + abs( st1.inf1h(i) - st2.inf1h(i) );
        diff.inf2h = diff.inf2h + abs( st1.inf2h(i) - st2.inf2h(i) );
        diff.indnc = diff.indnc + abs( st1.indnc(i) - st2.indnc(i) );
        diff.idmnc = diff.idmnc + abs( st1.idmnc(i) - st2.idmnc(i) );
    end           
                      
end