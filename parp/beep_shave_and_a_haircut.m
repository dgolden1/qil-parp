function beep_shave_and_a_haircut
% Make a beeping noise to the tune of "shave and a haircut"

% By Daniel Golden (dgolden1 at stanford dot edu) August 2012
% $Id$

tint = 0.2; % sec

beep;
pause(tint*2);
beep;
pause(tint);
beep;
pause(tint);
beep;
pause(tint*2);
beep;
pause(tint*4);
beep;
pause(tint*2);
beep;
